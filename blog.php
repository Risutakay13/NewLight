<h4><span><FONT COLOR="black">Ecoturismo en Zacatlán de las Manzanas.</FONT></span></h4>
<FONT COLOR="black">Su olor a pino y aire fresco lo hacen el lugar perfecto para despejarse y para acampar. Se encuentra a 40 minutos del centro de Zacatlán. Las piedras monumentales de más de 65 millones de años que parecen superpuestas lograrán robar tu atención. Adicionalmente, encontrarás caballos, cuatrimotos, tirolesas y un área para disfrutar antojitos tradicionales. </FONT>  </p>

